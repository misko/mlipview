export function getElementName(symbol) {
  return (ELEMENT_NAME_OVERRIDES[symbol] ?? ELEMENT_NAMES[symbol] ?? (symbol ?? 'X'));
}

export function getElementMass(symbol) {
  const value = ELEMENT_MASSES[symbol];
  return typeof value === 'number' ? value : null;
}

export function getElementColor(symbol) {
  return ELEMENT_COLORS[symbol] ?? '#9aa0a6';
}

export function getElementDisplay(symbol) {
  const name = getElementName(symbol);
  return { symbol: symbol ?? 'X', name, color: getElementColor(symbol), mass: getElementMass(symbol) };
}
